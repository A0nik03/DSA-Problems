class Solution
{
  public:
    //Function to find maximum of each subarray of size k.
    vector <int> max_of_subarrays(int *arr, int n, int k)
    {
        int maxi = INT_MIN;
        vector<int> maximum;
        int sum = 0;
        int cnt = 0;
        for(int i = 0; i < n ; i++){
            cnt++;
            if(arr[i] > maxi){
                maxi = arr[i];
            }
            
            if(cnt == k){
                cout<<maxi<<endl;
                maximum.push_back(maxi);
                cnt--;
            }
        }
        return maximum;
    }
};

